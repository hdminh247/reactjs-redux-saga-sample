export const ItemCarousel = (props) => {
  const {
    image = "",
    title = "",
    content = ""
  } = props;
  return (
    <div className={"item container"}>
      <div className={"description"}>
        <img alt={"item-carousel"} src={image} className={"img-fluid img-responsive"}
             onError={(e) => {
               e.target.onerror = null;
               e.target.src = "./carousel1.png";
             }}/>
        <div className={"text"}>
          <div className={"large"}>{title}</div>
          <div className={"small"} dangerouslySetInnerHTML={{ __html: content }}/>
        </div>
      </div>
    </div>
  );
};

class CarouselHome extends React.Component {
  constructor(props) {
    super(props);
    this.state = { activeIndex: 0 };
    this.next = this.next.bind(this);
    this.previous = this.previous.bind(this);
    this.goToIndex = this.goToIndex.bind(this);
    this.onExiting = this.onExiting.bind(this);
    this.onExited = this.onExited.bind(this);
  }

  onExiting() {
    this.animating = true;
  }

  onExited() {
    this.animating = false;
  }

  next() {
    if (this.animating) return;
    const nextIndex = this.state.activeIndex === items.length - 1 ? 0 : this.state.activeIndex + 1;
    this.setState({ activeIndex: nextIndex });
  }

  previous() {
    if (this.animating) return;
    const nextIndex = this.state.activeIndex === 0 ? items.length - 1 : this.state.activeIndex - 1;
    this.setState({ activeIndex: nextIndex });
  }

  goToIndex(newIndex) {
    if (this.animating) return;
    this.setState({ activeIndex: newIndex });
  }

  render() {
    const { activeIndex } = this.state;
    let baseSettings = {
      auto: true,
      autoplay: true,
      dots: false,
      infinite: true,
      speed: 1500,
      adaptiveHeight: true,
      centerPadding: 30,
      slidesToShow: 2,
      slidesToScroll: 2,
      responsive: [
        {
          breakpoint: 992,
          settings: {
            slidesToShow: 1,
            slidesToScroll: 1,
            infinite: true,
            dots: true,
            arrows: false
          }
        }
      ]
    };

    const {
      list = [],
      settings = {}
    } = this.props;

    return (
      <div className={"carousel-home-wrapper"}>
        <Slider
          {...baseSettings}
          {...settings}
        >
          {list.map(item => {
            return (
              <div className={"item"} key={uuidv1()}>
                <ItemCarousel {...item} />
              </div>
            );
          })}
        </Slider>
      </div>
    );
  }
}

